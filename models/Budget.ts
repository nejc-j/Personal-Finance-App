import mongoose from 'mongoose'

const BudgetSchema = new mongoose.Schema({
  category: {
    type: String,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
  startDate: {
    type: Date,
    required: true,
  },
  endDate: {
    type: Date,
    required: true,
  },
})

export default mongoose.models.Budget || mongoose.model('Budget', BudgetSchema)
